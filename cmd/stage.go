func getServiceMesh() ServiceMesh {
	//get the service mesh file
	path := viper.GetString("release")
	fullPath := path + "/servicemesh/" + meshName + ".yaml"
	valuesBytes, err := ioutil.ReadFile(fullPath)

	if err != nil {
		panic(err)
	}

	mesh := ServiceMesh{}
	err = yaml.Unmarshal(valuesBytes, &mesh)
	if err != nil {
		panic(err)
	}
	return mesh
}

func convertMeshToEnvVars(mesh ServiceMesh) map[string]string {
	env := make(map[string]string)

	for _, testSvc := range mesh.Test {
		env[testSvc] = branchName + "-" + testSvc
	}

	for _, stagingSvc := range mesh.Staging {
		env[stagingSvc] = "staging-" + stagingSvc
	}

	return env
}

func getSelectedBuilds(mesh ServiceMesh, config Config) []Build {
	var builds []Build
	for _, testSvc := range mesh.Test {
		for _, build := range config.Builds {
			if testSvc == build.Name {
				builds = append(builds, build)
			}
		}
	}
	return builds
}

type ServiceMesh struct {
	Name    string   `yaml:"Name"`
	Test    []string `yaml:"Test"`
	Staging []string `yaml:"Staging"`
}
